# -*- coding: utf-8 -*-

# =============================================================================
# Bitwise Operators
# =============================================================================
a = 0b00111100
b = 0b00001101

# AND和
print("a&b:"+bin(a&b))
# OR或
print("a|b:"+bin(a|b))
# XOR异或
print("a^b:"+bin(a^b))
# 二进制的一补
print("~a:"+bin(~a))
# 左移
print("a<<2"+bin(a))
# 右移
print("a>>2"+bin(a))

# =============================================================================
# Logical Operators 逻辑运算
# =============================================================================

# AND
if a and b:
    False

# OR
if a or b:
    True

# NOT
if not(a and b):
    True
# =============================================================================
# Membership Operators 成员运算
# =============================================================================
 
# in
if a in b:
    True
# not in 
if a not in b:
    True
# =============================================================================
# Identity Operators 身份运算
# =============================================================================

# is
if a is b:
    True

# is not 
if a is not b:
    True
    